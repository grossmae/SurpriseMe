import UIKit

extension UIColor {
    
    open class var smBlue: UIColor {
        get {
            return UIColor(red: 0.36, green: 0.82, blue: 1.0 , alpha: 1.0)
        }
    }
    
    open class var smDarkText: UIColor {
        get {
            return UIColor(red: 0.29, green: 0.29, blue: 0.29 , alpha: 1.0)
        }
    }

}
