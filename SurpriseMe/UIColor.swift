import UIKit

extension UIColor {
    
    open class var smBlue: UIColor {
        get {
            return UIColor(red: 0.36, green: 0.82, blue: 1.0 , alpha: 1.0)
        }
    }
    
    open class var smLightBlue: UIColor {
        get {
            return UIColor(red: 0.78, green: 1.0, blue: 1.0 , alpha: 1.0)
        }
    }
    
    open class var smDarkBlue: UIColor {
        get {
            return UIColor(red: 0.05, green: 0.21, blue: 0.33 , alpha: 1.0)
        }
    }
    
    open class var smYellow: UIColor {
        get {
            return UIColor(red: 1.0, green: 0.83, blue: 0.19 , alpha: 1.0)
        }
    }
    
    open class var smGray: UIColor {
        get {
            return UIColor(red: 1.0, green: 0.83, blue: 0.19 , alpha: 1.0)
        }
    }
    
    open class var smDarkText: UIColor {
        get {
            return UIColor(red: 0.60, green: 0.60, blue: 0.60 , alpha: 0.60)
        }
    }

}
